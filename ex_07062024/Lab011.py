# Escape sequence
print("Hello \"World\" World") # \" is used to print double quotes
print('I\'m Batman') # \' is used to print single quote
print("Hello\nWorld") # \n is used to print in a new line
print("Hello\tWorld") # \t is used to print tab space
print("Hello\aWorld") # \a is used to print alert
print("Hello\bWorld") # \b is used to print backspace
print("Hello\fWorld") # \f is used to print form feed
print("Hello\rWorld") # \r is used to print carriage return