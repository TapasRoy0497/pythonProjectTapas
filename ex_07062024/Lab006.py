# Take 2 int numbers as input from user and add them

num1 = input("Enter first number")
num2 = input("Enter second number")
result1 = num1 + num2
result2 = int(num1) + int(num2)

print("Result of num1 + num2 = " + result1)
print("Result of int(num1) + int(num2) = " + str(result2))

# + -> int - sum operation
# + -> str - string concat
# int to str -> (str)
# str to int -> (int)

