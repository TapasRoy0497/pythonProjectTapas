# In-built Functions
# Function -> A block of code which only runs when it is called, to perform a task call it repeatedly
# print() -> Prints the message to the console
print("Hello World")  # Output: Hello World

# print(), input(), type(), len(), range(), format(), max(), min(), reversed() -> These are some of the in-built functions in Python

name = "Michael"  # Stored in form of array
print(name)  # Output: Michael
print(type(name))  # Output: <class 'str'>
print(len(name))  # Output: 7
print(id(name))  # Output: Address of the variable in memory

str = "It's a great day!!"
print(str)  # Output: It's a great day!!
print(len(str))
print(id(str))
print(type(str))
print(str[0])  # Output: I
print(str[-1])  # Output: !, from reverse
print(str[-10])  # Output: r
print(str[0:5])  # Output: It's
